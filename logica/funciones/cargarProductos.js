module.exports = async function cargarProductos() {

    var textoSQL = `select * from Products`;

    return new Promise( (resolver, rechazar) => {
        cargarProductos.conexion.all( textoSQL,
            ( err, res ) => {
                ( err ? rechazar(err) : resolver(res) )
            })
    })

} // ()



